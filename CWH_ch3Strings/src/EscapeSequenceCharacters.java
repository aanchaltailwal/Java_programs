
public class EscapeSequenceCharacters {
	public static void main(String[]args) {
		System.out.println("I am Escape sequence \" double quote");     //  for double quotes\"
		
		System.out.println("who are you \\ broo");                      // for backslash \\
		
		System.out.println("I am Esacpe sequence \' singke quote");     // for single quote
		
		System.out.println("what the fuck \t is this");                 // for a tab
		
	}

}
