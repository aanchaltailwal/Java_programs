
public class IncrementDecrement {
	public static void main(String[]args) {
		int i = 56;
     	System.out.println(i++); // use i first and then increment
		System.out.println(i);   //increment of i
		System.out.println(++i); //first increment then i
		System.out.println(i);   //only i
		
		System.out.println(i--); //first i and then decrement
		System.out.println(i);   //decrement of i
		System.out.println(--i); //first decrement then i
		System.out.println(i);   //only i
		
	}
}
