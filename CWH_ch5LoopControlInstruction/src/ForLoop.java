
public class ForLoop {
	public static void main(String[]args) {
		for(int i=1; i<=10; i++) {
			System.out.println(i);
		}
//QUICK QUIZ
		//2n = 2, 4, 6, 8.....
		//2n+1 = 3, 5, 7, 9......
	    int n=5 ;
	    for(int j=0; j<n; j++) {
	    	System.out.println(2*j+1);
	    }
	    
//Decrementing for Loop	   
	    for(int k=5; k>0; k--) {
	    	System.out.println(k);
	    }
	    
//QUICK QUIZ
	    int m=7;
	    for(int l=1; l<m; l++) {
	    	System.out.println(l);
	    	
	    }
	    
	}

}
 