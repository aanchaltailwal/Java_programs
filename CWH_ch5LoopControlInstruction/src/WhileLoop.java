
public class WhileLoop {
		public static void main(String[]args) {
//WHILE LOOP
//1st example
			int i = 1;
			while(i<=3) {
				System.out.println(i);
				i++;
			}
//2nd example	
			int k = 0;
			while(k<5) {
				System.out.println(k);
				k++;
			}
//QUICK QUIZ
			int j = 1;
			System.out.println("THESE ARE THE NATURAL NUMBERS BETWEEN 100 TO 200");
			while(j<=200) {
				System.out.println(j);
				j++;
			}
		}
}
